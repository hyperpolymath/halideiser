/// Generate a standalone runner that loads an image, runs the pipeline, and
/// saves the output. Uses Halide's `load_image` / `save_image` helpers.
pub fn generate_runner(manifest: &Manifest) -> String {
    let name = &manifest.project.name;
    let pixel_type = &manifest.pipeline.bit_depth;

    let mut code = String::with_capacity(2048);

    code.push_str(&format!(
        "// Auto-generated runner for {} — DO NOT EDIT\n",
        name
    ));
    code.push_str("// SPDX-License-Identifier: PMPL-1.0-or-later\n\n");
    code.push_str("#include \"HalideBuffer.h\"\n");
    code.push_str("#include \"halide_image_io.h\"\n");
    code.push_str(&format!("#include \"{name}_generator.h\"\n\n"));
    code.push_str("#include <cstdio>\n");
    code.push_str("#include <cstdlib>\n\n");

    code.push_str("int main(int argc, char **argv) {\n");
    code.push_str("    if (argc < 3) {\n");
    code.push_str(&format!(
        "        fprintf(stderr, \"Usage: %s <input.{in_fmt}> <output.{out_fmt}>\\n\", argv[0]);\n",
        in_fmt = manifest.pipeline.input_format,
        out_fmt = manifest.pipeline.output_format,
    ));
    code.push_str("        return 1;\n");
    code.push_str("    }\n\n");

    code.push_str(&format!(
        "    Halide::Runtime::Buffer<{pixel_t}> input = \n\
         \x20       Halide::Tools::load_image(argv[1]);\n\n",
        pixel_t = pixel_type,
    ));

    code.push_str(&format!(
        "    Halide::Runtime::Buffer<{pixel_t}> output(\n\
         \x20       input.width(), input.height(), input.channels());\n\n",
        pixel_t = pixel_type,
    ));

    code.push_str(&format!(
        "    int error = {name}_generator(input, output);\n",
    ));
    code.push_str("    if (error) {\n");
    code.push_str("        fprintf(stderr, \"Pipeline failed with error %d\\n\", error);\n");
    code.push_str("        return error;\n");
    code.push_str("    }\n\n");

    code.push_str("    Halide::Tools::save_image(output, argv[2]);\n");
    code.push_str(&format!(
        "    printf(\"{name} pipeline completed: %s -> %s\\n\", argv[1], argv[2]);\n",
    ));
    code.push_str("    return 0;\n");
    code.push_str("}\n");

    code
}

